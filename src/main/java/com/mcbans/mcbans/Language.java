package com.mcbans.mcbans;

public class Language {

	public String getFormat() {
		return "";
	}

	public String getFormat(String a) {
		return a;
	}

	public String getFormat(String a, String b) {
		return a + " " + b;
	}

	public String getFormat(String a, String b, String c) {
		return a + " " + b + " " + c;
	}

	public String getFormat(String a, String b, String c, String d) {
		return a + " " + b + " " + c + " " + d;
	}
}
